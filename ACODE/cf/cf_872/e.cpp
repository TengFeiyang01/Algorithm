void solve() {
    int n, k;
    std::cin >> n >> k;
    std::vector<int> g[n];
    for (int i = 1; i < n; ++i) {
        int x, y;
        std::cin >> x >> y;
        x--, y--;
        g[x].emplace_back(y);
        g[y].emplace_back(x);
    }

    if (k == 2) {
        std::vector<ll> siz(n), f(n);
        std::function<void(int, int, int)> dfs = [&](int u, int fa, int d) {
            siz[u] = 1;
            f[0] += d;
            for (auto v : g[u]) {
                if (v == fa) continue;
                dfs(v, u, d + 1);
                siz[u] += siz[v];
            }    
        };
        dfs(0, -1, 0);
        ll ans = 0;
        std::cout << f[0] << "\n";
        std::function<void(int, int)> dfs1 = [&](int u, int fa) {
            for (auto v : g[u]) {
                if (v == fa) continue;
                f[v] = f[u] + n - siz[v] * 2;
                dfs1(v, u);
            }
        };
        dfs1(0, -1);
        ans = std::accumulate(f.begin(), f.end(), 0);
        std::cout << ans + (n  - 1) * n << "\n";
    }
}

