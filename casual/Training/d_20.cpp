void solve() {
    ll n, e, d;
    std::cin >> n >> e >> d;
    ll b = n - e * d + 2LL;
    ll delta = -4 * n + b * b;
    if (delta < 0 || (ll)sqrt(delta) * (ll)sqrt(delta) != delta) {
        std::cout << "NO\n";
        return;
    }
    ll t = sqrt(delta);
    std::cout << (b - t) / 2LL << " " << (b + t) / 2LL << "\n";
}

